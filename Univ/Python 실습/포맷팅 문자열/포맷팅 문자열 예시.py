total = 100
avg = 85.67
name = "John"

# 예제 1: %d와 %f 사용
print("총점: %3d 평균: %6.2f" % (total, avg))
# 출력 결과: 총점: 100 평균:  85.67
# %3d: 정수형으로 최소 3자리로 출력
# %6.2f: 실수형으로 최소 6자리, 소수점 이하 2자리로 출력

# 예제 2: %s 사용
print("이름: %10s" % name)
# 출력 결과: 이름:       John
# %10s: 문자열로 최소 10자리로 출력, 값이 10자리보다 짧으면 왼쪽에 공백 추가
